package com.tylx.leasephone.util;

import android.databinding.ViewDataBinding;
import android.support.v7.widget.RecyclerView;
import android.view.View;

/**
 * Created by track on 2017/3/6.
 */

public class ObjectViewHolder<E extends ViewDataBinding> extends RecyclerView.ViewHolder {

    private E binding;

    public E getBinding() {
        return (E) binding;
    }

    public void setBinding(E binding) {
        this.binding = binding;
    }

    public ObjectViewHolder(View itemView) {
        super(itemView);
    }
}
